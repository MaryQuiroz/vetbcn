export function getClinicPhotoUrl(clinicId: string, animalTypes: string[]): string {
  const category = animalTypes.find((a) => PHOTOS[a]?.length) ?? "default";
  const pool = PHOTOS[category] ?? PHOTOS["default"];
  const hash = clinicId.split("").reduce((acc, c) => acc + c.charCodeAt(0), 0);
  const photo = pool[hash % pool.length];
  return `https://images.unsplash.com/photo-${photo}?w=800&q=80&auto=format&fit=crop`;
}
